int	ft_atoi(const char *nptr)
{
	int	i;
	int	tab;
	int	sign;

	i = 0;
	tab = 0;
	while (((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32) && nptr[i])
		i++;
	sign = 1;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		tab = tab * 10;
		tab += (nptr[i] - '0');
		i++;
	}
	return (tab * sign);
}
